import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const currentDir = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(currentDir, "..");
const actionsDir = path.join(
  packageRoot,
  "src/live2d/tools/custom-tool-actions/actions",
);
const outputPath = path.join(
  packageRoot,
  "src/live2d/tools/custom-tool-actions/actions.generated.ts",
);

const actionFiles = (await fs.readdir(actionsDir))
  .filter((file) => file.endsWith(".ts") && !file.endsWith(".d.ts"))
  .sort();

const importLines = actionFiles.map(
  (file, index) =>
    `import actionDefinition${index} from "./actions/${file.replace(/\.ts$/, "")}";`,
);

const definitionEntries = actionFiles.map(
  (_file, index) => `  actionDefinition${index},`,
);

const generatedSource = `// This file is auto-generated by scripts/generate-custom-tool-actions.mjs.
// Do not edit it manually.

${importLines.join("\n")}

export const customToolActionDefinitions = [
${definitionEntries.join("\n")}
] as const;

export type CustomToolAction = Exclude<
  ReturnType<(typeof customToolActionDefinitions)[number]["normalize"]>,
  undefined
>;

export type CustomToolActionType = CustomToolAction["type"];

export const CUSTOM_TOOL_ACTION_TYPES = customToolActionDefinitions.map(
  (definition) => definition.type,
) as CustomToolActionType[];
`;

await fs.writeFile(outputPath, generatedSource);
